package com.md.taskmanagementsystem.enums;

/**
* Represents the priority level of a task.
* LOW: Tasks that are not urgent and can be completed when convenient
* MEDIUM: Tasks that should be completed soon but are not critical
* HIGH: Critical tasks that require immediate attention
*/
public enum TaskPriority {
   LOW,
   MEDIUM,
   HIGH,
   URGENT
} 
